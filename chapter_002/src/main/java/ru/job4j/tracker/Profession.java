package ru.job4j.tracker;

public class Profession {
    String name;

    String surname;

    String education;

    int birthday;

    public String getName() {
        return this.name;
    }
    public String getSurname() {
        return this.surname;
    }
    public String getEducation() {
        return this.education;
    }
    public int getBirthday() {
        return this.birthday;
    }
}
